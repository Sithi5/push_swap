#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	long long i;
	long long c;

	i = ft_strlen(s1);
	c = 0;
	while (s2[c])
	{
		s1[i] = s2[c];
		c++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
