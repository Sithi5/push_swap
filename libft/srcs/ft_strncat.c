#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	c;

	i = ft_strlen(s1);
	c = 0;
	while (c < n && s2[c])
	{
		s1[i] = s2[c];
		c++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
