#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*dest;
	long long		i;

	i = ft_strlen((char *)s1);
	if (!(dest = (char *)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
